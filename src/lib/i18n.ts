export type ErrorCodeValue = (typeof ErrorCode)[keyof typeof ErrorCode]

/**
 * 翻译函数。兼容已有调用方式，内部调用 i18next.t()。
 *
 * @param key - ErrorCode.* 或 dot-separated 路径（如 'panel.compress.title'）
 * @param params - 命名占位符替换（如 `{ name }` → 文件名）
 * @returns 翻译后的消息；未知 key 返回 key 自身（便于发现漏翻）
 */
export function t(key: string, params?: Record<string, string | number>): string {
  if (import.meta.env.DEV && !i18next.exists(key)) {
    console.warn(`[i18n] Missing translation: ${key}`)
  }
  return i18next.t(key, params as Record<string, unknown>) || key
}

/**
 * 简化调用：传入 ErrorCode 数组，返回第一条匹配的消息。
 * 主要用于统一"白名单通过/未通过"等场景。
 */
export function tOne(...codes: ErrorCodeValue[]): string {
  for (const code of codes) {
    if (i18next.exists(code)) return i18next.t(code)
  }
  return codes[0] ?? ''
}
